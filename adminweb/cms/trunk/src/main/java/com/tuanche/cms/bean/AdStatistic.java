package com.tuanche.cms.bean;

public class AdStatistic {
	private String date;
	private int cityId;
	private int channel;
	private int positionId;
	private int positionName;
	private int views;
	private int clicks;
	private int uniqClicks;
	private int adId;
	private int adType;
	private int start;
	private int limit;

	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getCityId() {
		return cityId;
	}
	public void setCityId(int cityId) {
		this.cityId = cityId;
	}
	public int getChannel() {
		return channel;
	}
	public void setChannel(int channel) {
		this.channel = channel;
	}
	public int getPositionId() {
		return positionId;
	}
	public void setPositionId(int positionId) {
		this.positionId = positionId;
	}
	public int getPositionName() {
		return positionName;
	}
	public void setPositionName(int positionName) {
		this.positionName = positionName;
	}
	public int getViews() {
		return views;
	}
	public void setViews(int views) {
		this.views = views;
	}
	public int getClicks() {
		return clicks;
	}
	public void setClicks(int clicks) {
		this.clicks = clicks;
	}
	public int getUniqClicks() {
		return uniqClicks;
	}
	public void setUniqClicks(int uniqClicks) {
		this.uniqClicks = uniqClicks;
	}
	public int getAdId() {
		return adId;
	}
	public void setAdId(int adId) {
		this.adId = adId;
	}
	public int getAdType() {
		return adType;
	}
	public void setAdType(int adType) {
		this.adType = adType;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getLimit() {
		return limit;
	}
	public void setLimit(int limit) {
		this.limit = limit;
	}

}
